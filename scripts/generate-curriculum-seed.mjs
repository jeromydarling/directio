#!/usr/bin/env node
/**
 * Convert agent-generated module JSON files into a D1 seed migration.
 *
 * Input:  /tmp/curriculum/*.json  (one file per module, matching the
 *         shape the generator agents were asked to produce)
 * Output: migrations/0007_seed_national_core.sql
 *
 * IDs are deterministic prefixed slugs so re-running this script with
 * the same inputs produces a byte-identical migration.
 */

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join } from "node:path";

const INPUT_DIR = "/tmp/curriculum";
const OUTPUT_FILE = "/home/user/directio/migrations/0007_seed_national_core.sql";

const PACK_ID = "cp_national_teen_core";
const VERSION_ID = "cpv_national_teen_core_1_0_0";
const COURSE_ID = "course_national_teen_core";

// SQL string escape for SQLite: double single-quotes.
const q = (s) => {
  if (s === null || s === undefined) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
};

const validateModule = (mod, filename) => {
  if (!mod.slug || !mod.title || !Array.isArray(mod.lessons))
    throw new Error(`${filename}: missing slug/title/lessons`);
  if (mod.lessons.length === 0) throw new Error(`${filename}: zero lessons`);
  for (const l of mod.lessons) {
    if (!l.slug || !l.title || !l.body)
      throw new Error(`${filename}: lesson missing slug/title/body`);
    if (!l.quiz || !Array.isArray(l.quiz.questions))
      throw new Error(`${filename}/${l.slug}: missing quiz.questions`);
    for (const [i, qst] of l.quiz.questions.entries()) {
      if (!qst.prompt || !Array.isArray(qst.choices) || qst.choices.length !== 4)
        throw new Error(`${filename}/${l.slug}/q${i}: bad shape (need 4 choices)`);
      if (typeof qst.correctIndex !== "number" || qst.correctIndex < 0 || qst.correctIndex > 3)
        throw new Error(`${filename}/${l.slug}/q${i}: correctIndex must be 0-3`);
    }
  }
};

const files = readdirSync(INPUT_DIR)
  .filter((f) => f.endsWith(".json"))
  .sort();
if (files.length === 0) {
  console.error("No JSON files in", INPUT_DIR);
  process.exit(1);
}

console.log(`Reading ${files.length} module files from ${INPUT_DIR}`);
const modules = files.map((f) => {
  const raw = readFileSync(join(INPUT_DIR, f), "utf8");
  const parsed = JSON.parse(raw);
  validateModule(parsed, f);
  return { ...parsed, _file: f };
});

// Stable ordering: the spec lists topics in a particular order; respect
// alphabetical filename order for now. The seeder can reorder later.
const lines = [];
lines.push("-- Seeded national-core teen driver education curriculum.");
lines.push("-- Generated by scripts/generate-curriculum-seed.mjs from agent output.");
lines.push("-- Do not edit by hand; regenerate from /tmp/curriculum/*.json.");
lines.push("");
lines.push("-- Pack and version");
lines.push(
  `INSERT INTO content_pack (id, slug, name, scope, jurisdiction, description, createdAt) VALUES (`,
);
lines.push(
  `  ${q(PACK_ID)}, ${q("national-teen-core")}, ${q("National Teen Driver Education Core")}, ${q("national")}, NULL, ${q("Reusable national core curriculum for teen driver education. Install + brand + edit in your tenant.")}, unixepoch('now') * 1000`,
);
lines.push(`);`);
lines.push("");
lines.push(
  `INSERT INTO content_pack_version (id, contentPackId, version, notes, publishedAt, createdAt) VALUES (`,
);
lines.push(
  `  ${q(VERSION_ID)}, ${q(PACK_ID)}, ${q("1.0.0")}, ${q("Initial release. 10 modules, ~40 lessons, ~200 quiz questions. Generated content; human review required before production use.")}, unixepoch('now') * 1000, unixepoch('now') * 1000`,
);
lines.push(`);`);
lines.push("");
lines.push(`INSERT INTO course (id, contentPackVersionId, slug, title, description, ordinal) VALUES (`);
lines.push(
  `  ${q(COURSE_ID)}, ${q(VERSION_ID)}, ${q("teen-driver-ed")}, ${q("Teen Driver Education")}, ${q("The full national-core teen driver education course. Ten modules covering safe driving fundamentals.")}, 0`,
);
lines.push(`);`);
lines.push("");

let moduleOrdinal = 0;
for (const m of modules) {
  const moduleId = `module_${m.slug}`;
  lines.push(`-- Module: ${m.title}`);
  lines.push(`INSERT INTO module (id, courseId, slug, title, description, ordinal) VALUES (`);
  lines.push(
    `  ${q(moduleId)}, ${q(COURSE_ID)}, ${q(m.slug)}, ${q(m.title)}, ${q(m.description ?? null)}, ${moduleOrdinal}`,
  );
  lines.push(`);`);

  let lessonOrdinal = 0;
  for (const l of m.lessons) {
    const lessonId = `lesson_${m.slug}_${l.slug}`;
    const quizId = `quiz_${m.slug}_${l.slug}`;

    lines.push(
      `INSERT INTO lesson (id, moduleId, slug, title, body, estimatedSeatMinutes, ordinal) VALUES (`,
    );
    lines.push(
      `  ${q(lessonId)}, ${q(moduleId)}, ${q(l.slug)}, ${q(l.title)}, ${q(l.body)}, ${l.estimatedSeatMinutes ?? 12}, ${lessonOrdinal}`,
    );
    lines.push(`);`);

    lines.push(`INSERT INTO quiz (id, lessonId, title, passingScore, shuffleQuestions) VALUES (`);
    lines.push(
      `  ${q(quizId)}, ${q(lessonId)}, ${q(l.quiz.title ?? `Quiz: ${l.title}`)}, ${l.quiz.passingScore ?? 80}, 1`,
    );
    lines.push(`);`);

    for (const [i, qst] of l.quiz.questions.entries()) {
      const questionId = `q_${m.slug}_${l.slug}_${i}`;
      const choicesJson = JSON.stringify(qst.choices);
      lines.push(
        `INSERT INTO quiz_question (id, quizId, prompt, choices, correctIndex, explanation, ordinal) VALUES (`,
      );
      lines.push(
        `  ${q(questionId)}, ${q(quizId)}, ${q(qst.prompt)}, ${q(choicesJson)}, ${qst.correctIndex}, ${q(qst.explanation ?? null)}, ${i}`,
      );
      lines.push(`);`);
    }
    lessonOrdinal++;
  }
  lines.push("");
  moduleOrdinal++;
}

const sql = lines.join("\n") + "\n";
writeFileSync(OUTPUT_FILE, sql);
console.log(`Wrote ${OUTPUT_FILE} (${sql.length} bytes)`);
console.log(`  ${modules.length} modules`);
console.log(`  ${modules.reduce((a, m) => a + m.lessons.length, 0)} lessons`);
console.log(
  `  ${modules.reduce((a, m) => a + m.lessons.reduce((b, l) => b + l.quiz.questions.length, 0), 0)} quiz questions`,
);
